import mongoose from "mongoose";

const customerSchema = new mongoose.Schema({
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  street: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  state: {
    type: String,
    required: true,
  },
  pincode: {
    type: Number,
    required: true,
  },
  country: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  cart:{
    type:Object
  },
  owner:{
    type:mongoose.Schema.Types.ObjectId,
    ref:"User"
  }
});

export const Customer = mongoose.model( "Customer" , customerSchema)
